package com.gamewolf.dbcrawler.model.base;

public class TaskMessage {
	
	String taskId;
	
	String event;

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

}
